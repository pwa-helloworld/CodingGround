window.onload = (event) => {
  OpenSection('About');
  // document.getElementById('AboutButton').style.background = "var(--light-blue);";
};

function OpenSection(topicName) {
  var i;
  var topicID = document.getElementsByClassName('topic-section');
  var btnID = document.getElementsByClassName('menu-btn');

  for (i = 0; i < topicID.length; i++) {
    topicID[i].style.display = "none";
    // btnID[i].style.background = "var(--dark-blue)";
  }

  document.getElementById(topicName).style.display = "block";
  // document.getElementById(topicName + "Button").style.display = "var(--light-blue);"

}